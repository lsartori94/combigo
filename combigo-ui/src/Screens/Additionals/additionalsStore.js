import { API_BASE } from '../../constants';

//Get drivers
export async function getAdditionals() {
  const response = await fetch(`${API_BASE}/additionals`);
  if (!response.ok) {
    throw new Error(`HTTP error! status: ${response.status}`);
  }
  const result = await response.json();
  return result;
}

export async function getAdditionalDetails(id) {
  const response = await fetch(`${API_BASE}/additionals/${id}`);
  if (!response.ok) {
    throw new Error(`HTTP error! status: ${response.status}`);
  }
  const result = await response.json();
  return result;
}

export async function saveAdditionalDetails(additional) {
  const response = await fetch(
    `${API_BASE}/additionals/${additional.id}`,
    {
      method: 'PUT',
      headers: {
        'Accept': 'application/json',
        'Content-Type': 'application/json'
      },
      body: JSON.stringify(additional)
    }
  );
  if (!response.ok) {
    const error = await response.text();
    throw new Error(error);
  }
  const result = await response.json();
  return result;
}

export async function deleteAdditional(name) {
  const response = await fetch(
    `${API_BASE}/additionals/${name}`,
    { method: 'DELETE'}
  );
  if (!response.ok) {
    const error = await response.text();
    throw new Error(error);
  }
  const result = await response.json();
  return result;
}

export async function createAdditional(additional) {
  const response = await fetch(
    `${API_BASE}/additionals`,
    {
      method: 'POST',
      headers: {
        'Accept': 'application/json',
        'Content-Type': 'application/json'
      },
      body: JSON.stringify(additional)
    }
  );
  if (!response.ok) {
    const error = await response.text();
    throw new Error(error);
  }
  const result = await response.json();
  return result;
}
